public class veiculo {
    private String Tipo;
    private String Ano;
    private String Cor;

    public void setCor(String Cor) {
        this.Cor = Cor;
    }
    public void setAno(String Ano) {
        this.Ano = Ano;
    }
    public void setTipo(String Tipo) {
        this.Tipo = Tipo;
    }
   public String getCor() {
        return Cor;
    }
    public String getAno() {
        return Ano;
    }
    public String getTipo() {
        return Tipo;
    }
}
